#include "main.h"
#include <stdio.h>


/**
 * main - checks for uppercase character
 * @argc: the character to be checked
 * @argv: the character to be checked
 * Return: 1 if c is uppercase, 0 otherwise
 */

int main(int argc, char *argv[])
{
	int i;

	i = 0;
	while (i <= argv[argc])
	{
		printf("%s", argv[0]);
		printf("\n");
	}
	return (0);
}
